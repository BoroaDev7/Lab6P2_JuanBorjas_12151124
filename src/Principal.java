    private boolean validarnombreRaza(String nombre){
        for (Raza raza : razas) {
            if(raza.getNombreRaza().equals(nombre))
                return true;
            
        }
        return false;
    }
    
    private boolean validarnombrePlaneta(String nombre){
        for (Planetas planet : planetas) {
            if(planet.getNombrePlaneta().equals(nombre))
                return true;
            
        }
        return false;
    }
    
